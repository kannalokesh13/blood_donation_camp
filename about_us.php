    <h3>Blood Donation In India</h3>
    <p>
Blood donations in India are conducted by organisations and hospitals through blood donation camps. Donors can also visit blood banks in hospitals to donate blood. 
Efforts by the government and advocacy groups over the years have helped bridge the gap between demand and supply. 
The regulatory framework for blood donation and blood bank management rests with the Central Drugs Standard Control Organisation, while technical bodies like the National Blood Transfusion Council and National AIDS Control Organisation formulate guidelines and recommendations for transfusion medicine and blood bank management.
Challenges persist with regards to regulation of blood banks and transfusion practices as the sector is largely fragmented with uneven distribution of blood banks and supply of blood in parts of the country.
 Donors are usually provided with refreshments after the procedure, which include glucose drinks, biscuits and fruits. Some organisations offer transportation facilities, as well as certificates or badges as gratitude
